    private void formMouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_formMouseWheelMoved
        if (evt.getWheelRotation() < 0) {
            scene.goUp();
        } else {
            scene.goDown();
        }

    }//GEN-LAST:event_formMouseWheelMoved

