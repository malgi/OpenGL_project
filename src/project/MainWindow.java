    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed

        switch (evt.getKeyCode()) {
            case KeyEvent.VK_S:
                scene.goBack();
                break;
            case KeyEvent.VK_W:
                scene.goStraight();
                break;
            case KeyEvent.VK_D:
                scene.turnRight();
                break;
            case KeyEvent.VK_A:
                scene.turnLeft();
                break;
            case KeyEvent.VK_H:
                scene.turnOffLight1();
                break;
            case KeyEvent.VK_J:
                scene.turnOffLight2();
                break;
            case KeyEvent.VK_G:
                scene.turnOffLight0();
                break;

        }
    }//GEN-LAST:event_formKeyPressed

